    private void eliminarIEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarIEActionPerformed
       if(tablaIngreso.getSelectedRowCount()!=0){
        if(tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 3).toString().equals("Seminario")||tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 3).toString().equals("Sesión"))   
            control.EliminarIngresoEgreso(tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 0).toString(), tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 4).toString(),datos2,"Ingreso");
        else
            control.EliminarOtroIngresoEgreso(tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 3).toString(),tablaIngreso.getValueAt(tablaIngreso.getSelectedRow(), 4).toString(), datos2,"Ingreso");
        }
        if(tablaEgreso.getSelectedRowCount()!=0){
            if(tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(), 1).toString().equals("Seminario")||tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(), 1).toString().equals("Sesión"))
                control.EliminarIngresoEgreso(tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(), 0).toString(), tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(), 2).toString(),datos2,"Egreso");  
            else
                control.EliminarOtroIngresoEgreso(tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(),1).toString(), tablaEgreso.getValueAt(tablaEgreso.getSelectedRow(),2).toString(), datos2,"Egreso");
        }
        JOptionPane.showMessageDialog(null, "Se ha eliminado correctamente y se han actualizado los ingresos y egresos totales."); 
        control.activarDesactivar(false, eliminarIE);
        tablaIngreso.setModel(new javax.swing.table.DefaultTableModel(new Object [][] {},new String [] {"Ingreso", "Total Efectivo (Bs)", "Total Débito (Bs)", "Razon", "Fecha"}){public boolean isCellEditable(int row, int column){return false;}});
        tablaEgreso.setModel(new javax.swing.table.DefaultTableModel(new Object [][] {},new String [] {"Egreso:", "Razón:", "Fecha:"}){public boolean isCellEditable(int row, int column){return false;}});
        datos2= new FechasXml();
        control.LlenarTablaIngresos(tablaIngreso, ingresoTotal, saldoTotal, datos2);
        control.LlenarTablaEgresos(tablaEgreso,egresoTotal,saldoTotal,datos2);
    }//GEN-LAST:event_eliminarIEActionPerformed

