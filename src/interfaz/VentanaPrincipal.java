    private void registrarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarSesionActionPerformed
    if(!busquedaSesion.getText().equals("Buscar por mes (MM/YYYY):"))
        JOptionPane.showMessageDialog(null, "Borre el campo de busqueda antes de continuar."); 
    else{
         boolean y = control.ValidarFecha(fechaSesion);
         if(y==false){
            boolean x = control.ComprobarFecha(fechaSesion.getDate(),datos2); //Comprobar si el objeto fecha actual se encuentra ya en la lista
            if(x==false)
                control.ActualizarListaFecha(fechaSesion.getDate(),datos2);
  
            if(control.ActualizarListaSesion( fechaSesion.getDate(),datos2)==true){
                tablaSesion.setModel(new javax.swing.table.DefaultTableModel(new Object [][] {},new String [] {"Fecha"}){public boolean isCellEditable(int row, int column){return false;}});
                tablaSesion.setRowSorter(null);
                control.LlenarTablaSesion(tablaSesion,datos2);
                fechaSesion.setDate(null);
                
            }
            else
                 JOptionPane.showMessageDialog(null, "Sesion ya existente. No se ha registro.");  
            
         }   
        }   
    }//GEN-LAST:event_registrarSesionActionPerformed

    private void registrarSeminarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarSeminarioActionPerformed
        if(!busquedaSeminario.getText().equals("Buscar por nombre:"))
        JOptionPane.showMessageDialog(null, "Borre el campo de busqueda antes de continuar.");
        else{
            boolean y = control.ValidarFecha(fechaSeminario);
            if(y==false){
                boolean x = control.ComprobarFecha(fechaSeminario.getDate(), datos2); //Comprobar si el objeto fecha actual se encuentra ya en la lista
                if(x==false)    
                    control.ActualizarListaFecha(fechaSeminario.getDate(),datos2);
                
                if(control.ValidarSeminario(this, nombreSeminario)){
                    control.ActualizarListaSeminario(nombreSeminario, fechaSeminario.getDate(), datos2);
                    tablaSeminario.setModel(new javax.swing.table.DefaultTableModel(new Object [][] {},new String [] {"Nombre", "Fecha"}){public boolean isCellEditable(int row, int column){return false;}});
                    tablaSeminario.setRowSorter(null);            
                    control.LlenarTablaSeminarios(tablaSeminario, datos2);
                    control.LlenarTablaSeminariosD(tablaSeminarioD, tablaSeminario);
                    control.RestablecerCamposSeminario(nombreSeminario, fechaSeminario);
                }
              
                else
                    JOptionPane.showMessageDialog(null, "Rellene los campos corretamente.");   
            }
        }
    }//GEN-LAST:event_registrarSeminarioActionPerformed

